package br.com.sicoob.capes.cadastro.negocio.vo;

/**
 * A Classe ContaVO.
 */
public class ContaVO {
	
	/** O atributo numeroBanco. */
	private String numeroBanco;
	
	/** O atributo nomeBanco. */
	private String nomeBanco;
	
	/** O atributo numeroConta. */
	private String numeroConta;
	
	/** O atributo numeroAgencia. */
	private String numeroAgencia;
	
	/** O atributo nomeAgencia. */
	private String nomeAgencia;
	
	/**
	 * @return the nomeBanco
	 */
	public String getNomeBanco() {
		return nomeBanco;
	}
	/**
	 * @param nomeBanco the nomeBanco to set
	 */
	public void setNomeBanco(String nomeBanco) {
		this.nomeBanco = nomeBanco;
	}
	/**
	 * @return the nomeAgencia
	 */
	public String getNomeAgencia() {
		return nomeAgencia;
	}
	/**
	 * @param nomeAgencia the nomeAgencia to set
	 */
	public void setNomeAgencia(String nomeAgencia) {
		this.nomeAgencia = nomeAgencia;
	}
	/**
	 * @return the numeroAgencia
	 */
	public String getNumeroAgencia() {
		return numeroAgencia;
	}
	/**
	 * @param numeroAgencia the numeroAgencia to set
	 */
	public void setNumeroAgencia(String numeroAgencia) {
		this.numeroAgencia = numeroAgencia;
	}
	/**
	 * @return the numeroConta
	 */
	public String getNumeroConta() {
		return numeroConta;
	}
	/**
	 * @param numeroConta the numeroConta to set
	 */
	public void setNumeroConta(String numeroConta) {
		this.numeroConta = numeroConta;
	}
	/**
	 * @return the numeroBanco
	 */
	public String getNumeroBanco() {
		return numeroBanco;
	}
	/**
	 * @param numeroBanco the numeroBanco to set
	 */
	public void setNumeroBanco(String numeroBanco) {
		this.numeroBanco = numeroBanco;
	}
}
